const originalText = new Map();
const textNodes = [];
const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT);

while (walker.nextNode()) {
  const node = walker.currentNode;
  const parent = node.parentElement;
  if (!parent || ["SCRIPT", "STYLE"].includes(parent.tagName)) continue;
  const normalized = node.nodeValue.replace(/\s+/g, " ").trim();
  if (!normalized) continue;
  originalText.set(node, node.nodeValue);
  textNodes.push({ node, normalized });
}

const toggles = document.querySelectorAll("[data-language-toggle]");

function applyLanguage(language) {
  const useSpanish = language === "es";
  document.documentElement.lang = useSpanish ? "es" : "en";

  textNodes.forEach(({ node, normalized }) => {
    node.nodeValue = useSpanish && spanish[normalized] ? spanish[normalized] : originalText.get(node);
  });

  const originalTitle = document.documentElement.dataset.originalTitle || document.title;
  document.documentElement.dataset.originalTitle = originalTitle;
  document.title = useSpanish ? (titleTranslations[originalTitle] || originalTitle) : originalTitle;

  toggles.forEach((toggle) => {
    toggle.textContent = useSpanish ? "EN" : "ES";
    toggle.setAttribute("aria-label", useSpanish ? "Switch to English" : "Cambiar a español");
  });

  localStorage.setItem("portfolio-language", useSpanish ? "es" : "en");
}

toggles.forEach((toggle) => {
  toggle.addEventListener("click", () => {
    applyLanguage(document.documentElement.lang === "es" ? "en" : "es");
  });
});

applyLanguage(localStorage.getItem("portfolio-language") === "es" ? "es" : "en");
